function sumThreeNumbers(arg1,arg2,arg3) {
    let firstNumber = Number(arg1)
    let secondNumber = Number(arg2)
    let thirdNumber = Number(arg3)

    let result = firstNumber + secondNumber + thirdNumber

    console.log(result);
}

sumThreeNumbers('2', '3', '4')	//9
sumThreeNumbers('1.5', '1.5', '-1')   //1
